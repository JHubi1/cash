/// A container for all currency localizations of a locale.
///
/// This class has no constructor. You can use
/// [CurrencyLocalizationContainer.resolveLocale] to get a container for a
/// specific locale.
///
/// [CurrencyLocalizationContainer.resolveLocaleFallback] can be used to get a
/// container for a locale, falling back to "en" if the locale is not available.
class CurrencyLocalizationContainer {
  /// The locale code of this container.
  final String locale;

  /// Get the localization for a specific currency code.
  ///
  /// Returns `null` if the currency is not available in this container.
  CurrencyLocalization? operator [](String currency) =>
      _currencies[currency.toLowerCase()];
  final Map<String, CurrencyLocalization> _currencies;

  CurrencyLocalizationContainer._({
    required this.locale,
    required Map<String, CurrencyLocalization> currencies,
  }) : _currencies = currencies.map((k, v) => MapEntry(k.toLowerCase(), v));

  /// Resolve a [CurrencyLocalizationContainer] for a specific locale.
  ///
  /// For optimization reasons, the static getter [english] should be used if
  /// you only want to access the English localizations specifically.
  static CurrencyLocalizationContainer? resolveLocale(String locale) =>
      _localizations.where((e) => e.locale == locale).firstOrNull;

  /// Resolve a [CurrencyLocalizationContainer] for a specific locale with
  /// fallback.
  static CurrencyLocalizationContainer resolveLocaleFallback(String locale) =>
      resolveLocale(locale) ?? english;

  static CurrencyLocalizationContainer get english => _containerEn;
}

/// A localization for a single currency.
class CurrencyLocalization {
  /// The display name of the currency.
  final String displayName;

  /// The singular form of the display count.
  ///
  /// This property might still not be provided by the localization.
  /// [displayName] is automatically used as a fallback if this is not provided.
  /// Check [hasDisplayCountSingular] to see if this was provided.
  final String displayCountSingular;

  /// Whether the singular form of the display count is provided.
  ///
  /// If this is false, [displayCountSingular] has been defaulted to the value
  /// of [displayName].
  final bool hasDisplayCountSingular;

  /// The plural form of the display count.
  /// This property might still not be provided by the localization.
  /// [displayName] is automatically used as a fallback if this is not provided.
  /// Check [hasDisplayCountPlural] to see if this was provided.
  final String displayCountPlural;

  /// Whether the plural form of the display count is provided.
  ///
  /// If this is false, [displayCountPlural] has been defaulted to the value of
  /// [displayName].
  final bool hasDisplayCountPlural;

  const CurrencyLocalization._({
    required this.displayName,
    String? displayCountSingular,
    String? displayCountPlural,
  }) : displayCountSingular = displayCountSingular ?? displayName,
       hasDisplayCountSingular = displayCountSingular != null,
       displayCountPlural = displayCountPlural ?? displayName,
       hasDisplayCountPlural = displayCountPlural != null;
}

